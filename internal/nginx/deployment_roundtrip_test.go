// TestVesselDeploymentCommentSpecialCharacters verifies the round-trip
// property for a curated set of deployment names that include spaces,
// special characters, unicode, and edge cases.
//
// **Validates: Requirements 6.2, 6.3**
func TestVesselDeploymentCommentSpecialCharacters(t *testing.T) {
	specialNames := []string{
		"my-app",
		"my app",
		"my_app",
		"My App v2.0",
		"app with spaces and more spaces",
		"app!@#$%^&*()",
		"app+equals=sign",
		"app[brackets]",
		"app{braces}",
		"app|pipe",
		"app;semicolon",
		"app'quote",
		`app"doublequote`,
		"app,comma.period",
		"app<less>greater",
		"app?question/slash",
		"app~tilde`backtick",
		"123numeric456",
		"UPPERCASE",
		"MixedCase-App_v1.2.3",
		// Note: names with leading/trailing whitespace are intentionally
		// excluded here. extractVesselDeployment trims the extracted value,
		// so "  leading spaces" would extract as "leading spaces" — the
		// round-trip only holds for names that are already trimmed.
		"a", // single character
		strings.Repeat("x", 60), // long name
		"unicode-café",
		"日本語",
		"emoji-🚀",
	}

	tmpDir := t.TempDir()
	sitesAvailable := filepath.Join(tmpDir, "sites-available")
	if err := os.MkdirAll(sitesAvailable, 0755); err != nil {
		t.Fatalf("failed to create sites-available: %v", err)
	}
	m := &Manager{configRoot: tmpDir}

	for i, name := range specialNames {
		siteName := fmt.Sprintf("special-%d", i)

		err := m.CreateSiteForDeployment(siteName, "special.example.com", 8080, "", name)
		if err != nil {
			t.Errorf("name=%q: CreateSiteForDeployment error: %v", name, err)
			continue
		}

		data, err := os.ReadFile(filepath.Join(sitesAvailable, siteName))
		if err != nil {
			t.Errorf("name=%q: ReadFile error: %v", name, err)
			continue
		}
		content := string(data)

		// First line must be exactly the comment.
		expectedFirstLine := "# vessel-deployment: " + name
		lines := strings.SplitN(content, "\n", 2)
		if lines[0] != expectedFirstLine {
			t.Errorf("name=%q: first line = %q, want %q", name, lines[0], expectedFirstLine)
		}

		// Round-trip must be exact.
		extracted := extractVesselDeployment(content)
		if extracted != name {
			t.Errorf("name=%q: round-trip failed: extractVesselDeployment = %q", name, extracted)
		}
	}
}

// TestExtractVesselDeploymentNeverPanicsProperty verifies that
// extractVesselDeployment never panics on any string input, including
// strings with arbitrary unicode, null bytes, and control characters.
//
// **Validates: Requirements 6.2**
func TestExtractVesselDeploymentNeverPanicsProperty(t *testing.T) {
	prop := func(content string) bool {
		// Must not panic — just call it.
		_ = extractVesselDeployment(content)
		return true
	}
	cfg := &quick.Config{MaxCount: 20}
	if err := quick.Check(prop, cfg); err != nil {
		t.Errorf("extractVesselDeployment panicked on some input: %v", err)
	}
}

// TestExtractVesselDeploymentReturnTypeProperty verifies that
// extractVesselDeployment always returns a string (never panics, always
// returns a value that is either empty or a trimmed non-empty string).
//
// **Validates: Requirements 6.2**
func TestExtractVesselDeploymentReturnTypeProperty(t *testing.T) {
	rng := rand.New(rand.NewSource(99)) //nolint:gosec
	const iterations = 20

	for i := 0; i < iterations; i++ {
		// Generate a random content string.
		length := rng.Intn(200)
		var sb strings.Builder
		for j := 0; j < length; j++ {
			// Use printable ASCII plus newlines for realistic nginx config content.
			r := rune(32 + rng.Intn(95))
			if rng.Intn(20) == 0 {
				r = '\n'
			}
			sb.WriteRune(r)
		}
		content := sb.String()

		result := extractVesselDeployment(content)

		// Result must not have leading or trailing whitespace (it is trimmed).
		if result != strings.TrimSpace(result) {
			t.Errorf("iteration %d: extractVesselDeployment returned untrimmed result %q", i, result)
		}

		// Result must not contain newlines.
		if strings.ContainsAny(result, "\n\r") {
			t.Errorf("iteration %d: extractVesselDeployment returned result with newline: %q", i, result)
		}

		// If the content contains the prefix, the result must be non-empty
		// (unless the name part itself is all whitespace).
		prefix := "# vessel-deployment:"
		for _, line := range strings.Split(content, "\n") {
			trimmed := strings.TrimSpace(line)
			if strings.HasPrefix(trimmed, prefix) {
				namePart := strings.TrimSpace(strings.TrimPrefix(trimmed, prefix))
				if namePart != "" && result == "" {
					t.Errorf("iteration %d: content has prefix with non-empty name %q but extractVesselDeployment returned empty", i, namePart)
				}
				break
			}
		}

		// Result must not contain any control characters (except it may be empty).
		for _, r := range result {
			if unicode.IsControl(r) {
				t.Errorf("iteration %d: extractVesselDeployment returned result with control character U+%04X: %q", i, r, result)
				break
			}
		}
	}
}
